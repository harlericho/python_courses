# Tipo de datos
# Strings
print("Hello")
# "hello"
# 'hello'
# 'Hello'
print(type( '''Hello'''))
print(type(8))
print("""hello""")

print("Bye" + "World")
print(6+8)


# Numbers
print(86)
print(8.6)
print(type(6.8))

# Booleans
print(True)
print(type(False))


# List
# Se puede cambiar los datos
print([2,4,6,8])
print(["a",8,6.8,True])
print(type(["a","b","c","d"]))
print([])
# Tuples
# No se puede cmabiar los datos
print((1,2,3,4,5,6))
print(type((1,2,3,"hello",5,6)))
print()

# Dictorionies
# Clave y valor
print({"name" :"carlos","alias":"harlericho","age":35})
print(type({"name" :"carlos","alias":"harlericho","age":35}))

# NoneType
print(type(None))







